#include <philo.h>

int	is_num(char *s)
{
	if (*s == '+')
		s++;
	while (*s)
	{
		if ('0' > *s || '9' < *s)
			return (0);
		s++;
	}
	return (1);
}

int	ft_atoi(const char *s)
{
	long	x;
	int		i;

	x = 0;
	i = 0;
	if (s[i] == '+')
		i++;
	while (s[i])
	{
		x *= 10;
		x += s[i] - '0';
		i++;
	}
	return ((int)x);
}
